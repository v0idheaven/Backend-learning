// arithematic operators
console.log(10 + 2) // addition
console.log(10 - 2) // subtraction
console.log(10 * 2) // multiplication
console.log(10 / 2) // division
console.log(10 % 3) // reminder
console.log(10 ** 2) // exponentiation

